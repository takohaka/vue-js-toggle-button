<template>
<label :class="className"
       :style="style"
       role="checkbox">
  <input type="checkbox"
         class="v-switch-input"
         @change.stop="toggle">
  <span class="v-switch-core"
        :style="coreStyle"
        :aria-checked="ariaChecked">
    <span class="v-switch-control" :style="controlStyle"></span>
  </span>
  <div v-if="labels">
    <span class="v-switch-label v-left" :style="labelStyle" v-if="toggled">{{labelChecked}}</span>
    <span class="v-switch-label v-right" :style="labelStyle" v-else>{{labelUnchecked}}</span>
  </div>
</label>
</template>

<script>
const DEF_CHECKED_COLOR = '#75C791'
const DEF_UNCHEKED_COLOR = '#bfcbd9'

const DEF_CHECKED_LABEL = 'on'
const DEF_UNCHECKED_LABEL = 'off'

const margin = 3

const contains = (object, title) => {
  return typeof object === 'object' && object.hasOwnProperty(title)
}

export default {
  name: 'ToggleButton',
  props: {
    value: {
      type: Boolean,
      default: false
    },
    disabled: {
      type: Boolean,
      default: false
    },
    sync: {
      type: Boolean,
      default: false
    },
    color: {
      type: [String, Object],
      validator (value) {
        return typeof value === 'object'
          ? (value.checked || value.unchecked)
          : typeof value === 'string'
      }
    },
    labels: {
      type: [Boolean, Object],
      default: false,
      validator (value) {
        return typeof value === 'object'
          ? (value.checked || value.unchecked)
          : typeof value === 'boolean'
      }
    },
    height: {
      type: Number,
      default: 22
    },
    width: {
      type: Number,
      default: 50
    },
    borderRadius: {
      type: Number,
      default: 999
    }
  },
  computed: {
    className () {
      let { toggled, disabled } = this

      return [
        'vue-js-switch',
        { toggled, disabled }
      ]
    },

    distance () {
      return this.width - this.height + margin
    },

    ariaChecked () {
      return this.toggled.toString()
    },

    style () {
      let { width, height } = this

      return {
        'line-height': this.height + 'px',
        'height': this.height + 'px',
      }
    },

    colorChecked () {
      if (typeof this.color !== 'object') {
        return this.color || DEF_CHECKED_COLOR
      }

      return contains(this.color, 'checked')
        ? this.color.checked
        : DEF_CHECKED_COLOR
    },

    colorUnchecked () {
      return contains(this.color, 'unchecked')
        ? this.color.unchecked
        : DEF_UNCHEKED_COLOR
    },

    colorCurrent () {
      return this.toggled ? this.colorChecked : this.colorUnchecked
    },

    labelChecked () {
      return contains(this.labels, 'checked')
        ? this.labels.checked
        : DEF_CHECKED_LABEL
    },

    labelUnchecked () {
      return contains(this.labels, 'unchecked')
        ? this.labels.unchecked
        : DEF_UNCHECKED_LABEL
    },

    coreStyle () {
      return {
        'background-color': this.colorCurrent,
        'height': this.height + 'px',
        'width' : this.width + 'px',
        'border-radius': this.borderRadius + 'px'
      }
    },

    controlStyle () {
      const translateX = this.toggled ? this.distance : margin
      const style = {
        'height': this.height - 6 + 'px',
        'width': this.height - 6 + 'px',
        'border-radius': Math.round(this.borderRadius * ((this.height - 6) / this.height)) + 'px',
        'transform': `translate(${translateX}px, ${margin}px)`
      }
      return style
    },

    labelStyle () {
      return {
        'line-height': this.height + 'px',
        'height': this.height + 'px'
      }
    },

  },
  watch: {
    value (value) {
      if (this.sync) {
        this.toggled = value
      }
    }
  },
  data () {
    return {
      toggled: this.value
    }
  },
  methods: {
    toggle (event) {
      this.toggled = !this.toggled
      this.$emit('change', { value: this.toggled, srcEvent: event })
    }
  }
}
</script>

